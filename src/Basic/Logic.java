package Basic;

/**
 * 逻辑运算符
 * @author dingy
 *
 */
public class Logic {

	public static void main(String[] args) {
	     boolean a = true;
	     boolean b = false;
	     System.out.println("a && b = " + (a&&b));
	     System.out.println("a || b = " + (a||b) );
	     System.out.println("!(a && b) = " + !(a && b));
	}

}
